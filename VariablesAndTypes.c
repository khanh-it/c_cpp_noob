#include <stdio.h>

int Variables_and_Types() {
    char _char;
    unsigned char _uchar;
    int _int;
    unsigned int _uint;
    short _short;
    unsigned short _ushort;
    long _long;
    unsigned long _ulong;
    long long _llong;
    unsigned long long _ullong;
    //
    printf("char = %c\n", _char);
    printf("unsigned char = %c\n", _uchar);
    printf("int = %d\n", _int);
    printf("unsigned int = %u\n", _uint);
    printf("short = %d\n", _short);
    printf("unsigned short = %u\n", _ushort);
    printf("long = %d\n", _long);
    printf("unsigned long = %u\n", _ulong);
    printf("long long = %u\n", _llong);
    printf("unsigned long long = %u\n", _ullong);
    //
    return 0;
}

